import turtle

turtle.color("blue",'cyan')
turtle.speed(5)
turtle.pensize(2)

# Square
turtle.begin_fill()
turtle.forward(100)
turtle.setheading(90)
turtle.forward(100)
turtle.setheading(180)
turtle.forward(100)
turtle.setheading(270)
turtle.forward(100)
turtle.end_fill()

turtle.penup()
turtle.setheading(180)
turtle.forward(10)
turtle.pendown()

turtle.begin_fill()
turtle.setheading(90)
turtle.forward(100)
turtle.setheading(180)
turtle.forward(100)
turtle.setheading(270)
turtle.forward(100)
turtle.setheading(360)
turtle.forward(100)
turtle.end_fill()

turtle.penup()
turtle.setheading(270)
turtle.forward(10)
turtle.pendown()

turtle.begin_fill()
turtle.setheading(180)
turtle.forward(100)
turtle.setheading(270)
turtle.forward(100)
turtle.setheading(360)
turtle.forward(100)
turtle.setheading(360+90)
turtle.forward(100)
turtle.end_fill()

turtle.penup()
turtle.setheading(360)
turtle.forward(10)
turtle.pendown()

turtle.begin_fill()
turtle.setheading(360)
turtle.forward(100)
turtle.setheading(360-90)
turtle.forward(100)
turtle.setheading(360+180)
turtle.forward(100)
turtle.setheading(360-270)
turtle.forward(100)
turtle.end_fill()


turtle.done() 